package Calculadora;

public class celcius {
    public static void main(String[] args) {


            int celcius = 15;
            float convert = (float) 1.8;
            int fatorFahrenheit = 32;
            double calCelcius = (celcius * convert) + fatorFahrenheit;
            System.out.println(calCelcius);

    }
}
